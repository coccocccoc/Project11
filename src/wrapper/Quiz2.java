package wrapper;

public class Quiz2 {

	public static void main(String[] args) {

		// 다른 타입 -> 문자열
		// toString: 데이터를 String으로 반환하는 함수

		// 10 -> "10"
		String str1 = Integer.toString(10);

		// 1.123 -> "1.123"
		String str2 = Double.toString(1.123);

		// 'c' -> "c"
		String str3 = Character.toString('c');

		// true -> "true"
		String str4 = Boolean.toString(true);

	}

}
